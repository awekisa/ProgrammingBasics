using System;

class NumbersFromOneToN
{
    static void Main()
    {
        
        int inputNumber = int.Parse(Console.ReadLine());
        Console.WriteLine();
        for (int one = 1; one <= inputNumber; one++)
        {
            if (one <= inputNumber)
            {
                Console.WriteLine(one);
            }
            else
            {
                Console.WriteLine(one);
            }
        }

    }
}

